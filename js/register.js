$("#agreementBox").on("click",function(){
    if($(this).is(':checked')){
        $("#registerBtn").attr("disabled",false)
        $("#registerBtn").removeClass("btn_disabled");
    }else{
        $("#registerBtn").attr("disabled",true)
        $("#registerBtn").addClass("btn_disabled");
    }
})
